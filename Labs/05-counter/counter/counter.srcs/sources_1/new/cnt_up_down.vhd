library ieee;
use ieee.std_logic_1164.all;
use ieee.numeric_std.all;

------------------------------------------------------------------------
-- Entity declaration for n-bit counter
------------------------------------------------------------------------
entity cnt_up_down is
    generic(
        g_CNT_WIDTH : natural := 4      -- Number of bits for counter --cnt_width: šíøka èitaèe, poèet vodièù,
    );
    port(
        clk      : in  std_logic;       -- Main clock
        reset    : in  std_logic;       -- Synchronous reset
        en_i     : in  std_logic;       -- Enable input
        cnt_up_i : in  std_logic;       -- Direction of the counter
        cnt_o    : out std_logic_vector(g_CNT_WIDTH - 1 downto 0) -- 4-1 downto 0, šíøka cnt_o bude 4 bity
    );
end entity cnt_up_down;

------------------------------------------------------------------------
-- Architecture body for n-bit counter
------------------------------------------------------------------------
architecture behavioral of cnt_up_down is

    -- Local counter
    signal s_cnt_local : unsigned(g_CNT_WIDTH - 1 downto 0);    --vhdl neumožnuje èíst z výstupu, musíme definovat nový signál

begin
    --------------------------------------------------------------------
    -- p_cnt_up_down:
    -- Clocked process with synchronous reset which implements n-bit 
    -- up/down counter.
    --------------------------------------------------------------------
    p_cnt_up_down : process(clk)
    begin
        if rising_edge(clk) then    --spouští se s nástupnou hranou(rising_edge), kdyby sestupná tak falling_edge
        
            if (reset = '1') then               -- Synchronous reset    --asynchronní reset, radek 50 a 51 pøesunout nad if rising_edge a za clk 48 pøidat , reset
                s_cnt_local <= (others => '0'); -- Clear all bits   --reset v 1, vymažeme èítaè, '0' se použije protože nevíme kolika bitový je èitaè

            elsif (en_i = '1') then       -- Test if counter is enabled  --když enable v 1, pøièteme 1, když v 0, nedìje se nic


                -- TEST COUNTER DIRECTION HERE


                s_cnt_local <= s_cnt_local + 1;


            end if;
        end if;
    end process p_cnt_up_down;

    -- Output must be retyped from "unsigned" to "std_logic_vector"
    cnt_o <= std_logic_vector(s_cnt_local);

end architecture behavioral;
